fun main() {
    // Print Hello World
    println("Hello, World!")

    // Define variables
    val a = 10
    val b = 20

    // Perform basic arithmetic
    val sum = a + b
    val difference = b - a
    val product = a * b
    val quotient = b.toDouble() / a

    // Print the results
    println("Sum: $sum")
    println("Difference: $difference")
    println("Product: $product")
    println("Quotient: $quotient")
}
